// Fun√ß√£o para gerar lista inicial de ganhadores
export function gerarListaInicial(quantidade = 50) {
  const ganhadores = [];
  const agora = new Date();
  
  for (let i = 0; i < quantidade; i++) {
    const ganhador = gerarGanhadorAleatorio();
    // Espalha os timestamps ao longo das √∫ltimas 24 horas
    ganhador.timestamp = new Date(agora.getTime() - (Math.random() * 24 * 60 * 60 * 1000));
    ganhadores.push(ganhador);
  }
  
  // Ordena por timestamp (mais recente primeiro)
  return ganhadores.sort((a, b) => b.timestamp - a.timestamp);
}

